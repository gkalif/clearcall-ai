import os

# The exact binary header for a standard blank WAV audio track
wav_header = bytes([
    0x52, 0x49, 0x46, 0x46, 0x24, 0x08, 0x00, 0x00,
    0x57, 0x41, 0x56, 0x45, 0x66, 0x6D, 0x74, 0x20,
    0x10, 0x00, 0x00, 0x00, 0x01, 0x00, 0x01, 0x00,
    0x22, 0x56, 0x00, 0x00, 0x44, 0xAC, 0x00, 0x00,
    0x02, 0x00, 0x10, 0x00, 0x64, 0x61, 0x74, 0x61,
    0x00, 0x08, 0x00, 0x00,
])

# Create a 2-second silent audio data block
audio_data = bytes(44100 * 2) 

# Write the file directly to your folder
filename = "sample.wav"
with open(filename, "wb") as f:
    f.write(wav_header + audio_data)

print(f"✅ Successfully generated a valid test audio file: {os.path.abspath(filename)}")